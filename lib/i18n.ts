// The English tree defines the canonical shape. We deep-widen literal types to
// their base types (string) so both language trees satisfy the same interface.
type Widen<T> = T extends string
  ? string
  : T extends readonly (infer U)[]
    ? readonly Widen<U>[]
    : T extends object
      ? { [K in keyof T]: Widen<T[K]> }
      : T;

export type Translation = Widen<(typeof translations)["en"]>;
